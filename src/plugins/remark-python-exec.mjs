/**
 * remark-python-exec
 *
 * A remark plugin that finds fenced code blocks tagged with `python exec`
 * and injects their pre-generated output (stdout text and matplotlib figures)
 * as styled HTML blocks right after the code.
 *
 * Outputs are read from a cache file produced by scripts/pregenerate-python.mjs.
 *
 * Usage in Markdown:
 *
 *   ```python exec
 *   print("Hello from Python!")
 *   ```
 */

import { visit } from "unist-util-visit";
import { createHash } from "node:crypto";
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const CACHE_FILE = path.join(__dirname, ".python-exec-cache.json");

function hashCode(code) {
  return createHash("sha256").update(code).digest("hex");
}

export default function remarkPythonExec() {
  return function (tree) {
    // Load cache lazily so it picks up the file generated by the pre-generation script
    let cache = {};
    try {
      cache = JSON.parse(readFileSync(CACHE_FILE, "utf-8"));
    } catch (err) {
      console.warn("[remark-python-exec] Failed to load cache. Run `node scripts/pregenerate-python.mjs` first.");
    }

    const replacements = [];

    visit(tree, "code", (node, index, parent) => {
      if (!node.lang || !node.lang.startsWith("python")) return;
      if (!node.meta || !node.meta.includes("exec")) return;

      replacements.push({ node, index, parent });
    });

    for (const { node, index, parent } of replacements.reverse()) {
      const hash = hashCode(node.value);
      const result = cache[hash] || { stdout: "", images: [], error: "No cached output found. Run `node scripts/pregenerate-python.mjs` to generate." };

      const outputNodes = [];

      if (result.stdout && result.stdout.trim()) {
        outputNodes.push({
          type: "html",
          value: `<div class="py-exec-output"><div class="py-exec-label">Output</div><pre class="py-exec-pre">${escapeHtml(result.stdout)}</pre></div>`,
        });
      }

      if (result.images && result.images.length > 0) {
        for (const img of result.images) {
          outputNodes.push({
            type: "html",
            value: `<div class="py-exec-output py-exec-figure"><div class="py-exec-label">Figure</div><img src="data:image/png;base64,${img}" alt="Python plot output" class="py-exec-img" /></div>`,
          });
        }
      }

      if (result.error) {
        outputNodes.push({
          type: "html",
          value: `<div class="py-exec-output py-exec-error"><div class="py-exec-label">Error</div><pre class="py-exec-pre">${escapeHtml(result.error)}</pre></div>`,
        });
      }

      if (outputNodes.length > 0) {
        parent.children.splice(index + 1, 0, ...outputNodes);
      }
    }
  };
}

function escapeHtml(str) {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}
